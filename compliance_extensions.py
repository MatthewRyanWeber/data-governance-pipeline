# ═════════════════════════════════════════════════════════════════════════════
#  1. ComplianceMonitor  —  continuous hourly controls monitoring
# ═════════════════════════════════════════════════════════════════════════════

class ComplianceMonitor:
    """
    Run automated checks across all governance controls and log the results
    to the audit ledger.  Designed to be called on a schedule (e.g. hourly)
    to provide continuous compliance visibility rather than point-in-time
    snapshots at pipeline run time.

    Each check returns CONTROL_OK or CONTROL_FAILED and is logged as a
    governance event so the tamper-evident audit chain captures every
    monitoring pass.

    Controls checked
    ────────────────
    AUDIT_LEDGER_INTACT         Ledger files exist and the hash chain is
                                unbroken (no tampering detected).
    ENCRYPTION_KEY_PRESENT      Encryption key version file exists and
                                contains at least one active key.
    CONSENT_DB_REACHABLE        The SQLite consent database file exists and
                                can be opened.
    BAA_REGISTRY_CURRENT        No BAA entries are expired; warn if any
                                expire within warn_days.
    IRB_REGISTRY_CURRENT        No IRB protocol entries are expired; warn
                                if any expire within warn_days.
    PURPOSE_REGISTRY_PRESENT    The purpose limitation registry file exists
                                and is non-empty.
    VENDOR_RISK_CURRENT         No vendor reviews are overdue; warn if any
                                are due within warn_days.
    LOG_DIR_WRITABLE            The governance log directory is writable —
                                a failed write would silently break auditing.

    Quick-start
    ───────────
        from compliance_extensions import ComplianceMonitor
        from pipeline_v3 import GovernanceLogger

        gov     = GovernanceLogger(run_id="monitor_001", src="scheduler")
        monitor = ComplianceMonitor(gov)

        results = monitor.run_all()
        monitor.save_report("compliance_status.html")

        # Run on a schedule
        import schedule, time
        schedule.every().hour.do(monitor.run_all)
        while True:
            schedule.run_pending()
            time.sleep(60)
    """

    # Control IDs — used as event action strings and report keys
    CTRL_LEDGER          = "AUDIT_LEDGER_INTACT"
    CTRL_ENCRYPTION      = "ENCRYPTION_KEY_PRESENT"
    CTRL_CONSENT         = "CONSENT_DB_REACHABLE"
    CTRL_BAA             = "BAA_REGISTRY_CURRENT"
    CTRL_IRB             = "IRB_REGISTRY_CURRENT"
    CTRL_PURPOSE         = "PURPOSE_REGISTRY_PRESENT"
    CTRL_VENDOR          = "VENDOR_RISK_CURRENT"
    CTRL_LOG_DIR         = "LOG_DIR_WRITABLE"

    def __init__(
        self,
        gov,
        warn_days:      int  = 30,
        dry_run:        bool = False,
    ) -> None:
        """
        Parameters
        ──────────
        gov         GovernanceLogger for audit events.
        warn_days   Controls that involve expiry dates will warn when
                    expiry is within this many days.
        dry_run     Run all checks and produce reports without writing
                    CONTROL_OK / CONTROL_FAILED events to the ledger.
        """
        self.gov       = gov
        self.warn_days = warn_days
        self.dry_run   = dry_run
        self._results: List[Dict] = []

    # ── Public API ────────────────────────────────────────────────────────────

    def run_all(self) -> List[Dict]:
        """
        Execute all controls checks and return a list of result dicts.
        Each result has keys: control_id, status, detail, checked_at.
        Status is one of: "OK", "WARN", "FAIL".
        """
        self._results = []
        checks = [
            (self.CTRL_LOG_DIR,     self._check_log_dir_writable),
            (self.CTRL_LEDGER,      self._check_audit_ledger),
            (self.CTRL_ENCRYPTION,  self._check_encryption_key),
            (self.CTRL_CONSENT,     self._check_consent_db),
            (self.CTRL_BAA,         self._check_baa_registry),
            (self.CTRL_IRB,         self._check_irb_registry),
            (self.CTRL_PURPOSE,     self._check_purpose_registry),
            (self.CTRL_VENDOR,      self._check_vendor_risk),
        ]
        for ctrl_id, check_fn in checks:
            try:
                status, detail = check_fn()
            except Exception as exc:  # pylint: disable=broad-exception-caught
                logger.warning("ComplianceMonitor: check %s raised: %s", ctrl_id, exc)
                status, detail = "FAIL", f"Check raised an exception: {exc}"

            result = {
                "control_id": ctrl_id,
                "status":     status,
                "detail":     detail,
                "checked_at": _iso(_now_utc()),
            }
            self._results.append(result)

            if not self.dry_run:
                level = "INFO" if status == "OK" else ("WARN" if status == "WARN" else "ERROR")
                self.gov._event(  # type: ignore[attr-defined]
                    "COMPLIANCE",
                    f"CONTROL_{'OK' if status == 'OK' else 'FAILED'}",
                    {"control_id": ctrl_id, "status": status, "detail": detail},
                    level=level,
                )

            icon = "✅" if status == "OK" else ("⚠️ " if status == "WARN" else "❌")
            print(f"  {icon}  [{ctrl_id}] {status} — {detail}")

        passed = sum(1 for r in self._results if r["status"] == "OK")
        warned = sum(1 for r in self._results if r["status"] == "WARN")
        failed = sum(1 for r in self._results if r["status"] == "FAIL")
        print(f"\n  Controls: {passed} OK  |  {warned} WARN  |  {failed} FAIL")
        return self._results

    def run_check(self, control_id: str) -> Dict:
        """Run a single named control check and return its result dict."""
        dispatch = {
            self.CTRL_LOG_DIR:    self._check_log_dir_writable,
            self.CTRL_LEDGER:     self._check_audit_ledger,
            self.CTRL_ENCRYPTION: self._check_encryption_key,
            self.CTRL_CONSENT:    self._check_consent_db,
            self.CTRL_BAA:        self._check_baa_registry,
            self.CTRL_IRB:        self._check_irb_registry,
            self.CTRL_PURPOSE:    self._check_purpose_registry,
            self.CTRL_VENDOR:     self._check_vendor_risk,
        }
        if control_id not in dispatch:
            raise ValueError(
                f"ComplianceMonitor: unknown control_id '{control_id}'. "
                f"Valid: {list(dispatch.keys())}"
            )
        status, detail = dispatch[control_id]()
        return {"control_id": control_id, "status": status,
                "detail": detail, "checked_at": _iso(_now_utc())}

    def save_report(
        self,
        path: Union[str, pathlib.Path],
        fmt:  str = "html",
    ) -> pathlib.Path:
        """Write the last run_all() results as HTML (default) or JSON."""
        if not self._results:
            raise RuntimeError(
                "ComplianceMonitor: call run_all() before save_report()."
            )
        path = pathlib.Path(path)
        if fmt == "json":
            path.write_text(
                json.dumps(self._results, indent=2), encoding="utf-8"
            )
        else:
            path.write_text(self._build_html(), encoding="utf-8")
        logger.info("ComplianceMonitor: report written → %s", path)
        return path

    # ── Individual control checks ─────────────────────────────────────────────

    def _check_log_dir_writable(self) -> tuple:
        """Verify the governance log directory is writable."""
        log_dir = pathlib.Path(self.gov.log_dir)  # type: ignore[attr-defined]
        if not log_dir.exists():
            return "FAIL", f"Log directory does not exist: {log_dir}"
        probe = log_dir / ".write_probe"
        try:
            probe.write_text("probe", encoding="utf-8")
            probe.unlink()
            return "OK", f"Log directory writable: {log_dir}"
        except OSError as exc:
            return "FAIL", f"Log directory not writable: {exc}"

    def _check_audit_ledger(self) -> tuple:
        """Verify at least one audit ledger file exists and is non-empty."""
        log_dir = pathlib.Path(self.gov.log_dir)  # type: ignore[attr-defined]
        ledger_files = sorted(log_dir.glob("audit_ledger*.jsonl"))
        if not ledger_files:
            return "WARN", "No audit ledger files found — pipeline may not have run yet."
        total_events = 0
        for f in ledger_files:
            lines = [l for l in f.read_text(encoding="utf-8").splitlines() if l.strip()]
            total_events += len(lines)
        if total_events == 0:
            return "WARN", "Ledger files exist but contain no events."
        return "OK", f"{len(ledger_files)} ledger file(s), {total_events} event(s) total."

    def _check_encryption_key(self) -> tuple:
        """Verify the encryption key version file exists and has active keys."""
        key_file = pathlib.Path(self.gov.log_dir) / "encryption_key_versions.json"  # type: ignore[attr-defined]
        if not key_file.exists():
            return "WARN", "Encryption key version file not found — ColumnEncryptor not yet used."
        try:
            data = json.loads(key_file.read_text(encoding="utf-8"))
            if not data:
                return "WARN", "Encryption key file is empty — no keys registered."
            return "OK", f"{len(data)} encryption key version(s) registered."
        except Exception as exc:  # pylint: disable=broad-exception-caught
            return "FAIL", f"Could not read encryption key file: {exc}"

    def _check_consent_db(self) -> tuple:
        """Verify the consent SQLite database exists and is openable."""
        consent_db = pathlib.Path(self.gov.log_dir) / "consent.db"  # type: ignore[attr-defined]
        if not consent_db.exists():
            return "WARN", "Consent database not found — ConsentManager not yet used."
        try:
            import sqlite3
            conn = sqlite3.connect(str(consent_db), timeout=5)
            conn.execute("SELECT COUNT(*) FROM consent_records")
            count = conn.execute(
                "SELECT COUNT(*) FROM consent_records"
            ).fetchone()[0]
            conn.close()
            return "OK", f"Consent database reachable — {count} consent record(s)."
        except Exception as exc:  # pylint: disable=broad-exception-caught
            return "FAIL", f"Consent database check failed: {exc}"

    def _check_baa_registry(self) -> tuple:
        """Check BAA registry for expired or near-expiry entries."""
        baa_file = pathlib.Path(self.gov.log_dir) / "baa_registry.json"  # type: ignore[attr-defined]
        if not baa_file.exists():
            return "WARN", "BAA registry not found — BAATracker not yet used."
        try:
            registry = json.loads(baa_file.read_text(encoding="utf-8"))
        except Exception as exc:  # pylint: disable=broad-exception-caught
            return "FAIL", f"Could not read BAA registry: {exc}"
        if not registry:
            return "WARN", "BAA registry is empty — no BAAs registered."

        today   = _now_utc().date()
        expired, expiring = [], []
        for dest_id, rec in registry.items():
            expiry = datetime.fromisoformat(rec["expiry_date"]).date()
            days   = (expiry - today).days
            if days < 0:
                expired.append(dest_id)
            elif days <= self.warn_days:
                expiring.append(f"{dest_id} ({days}d)")

        if expired:
            return "FAIL", f"{len(expired)} expired BAA(s): {expired}"
        if expiring:
            return "WARN", f"BAA(s) expiring soon: {expiring}"
        return "OK", f"{len(registry)} active BAA(s), none expiring within {self.warn_days} days."

    def _check_irb_registry(self) -> tuple:
        """Check IRB registry for expired or near-expiry protocols."""
        irb_file = pathlib.Path(self.gov.log_dir) / "irb_registry.json"  # type: ignore[attr-defined]
        if not irb_file.exists():
            return "WARN", "IRB registry not found — IRBApprovalGate not yet used."
        try:
            registry = json.loads(irb_file.read_text(encoding="utf-8"))
        except Exception as exc:  # pylint: disable=broad-exception-caught
            return "FAIL", f"Could not read IRB registry: {exc}"
        if not registry:
            return "WARN", "IRB registry is empty."

        today    = _now_utc().date()
        expired, expiring = [], []
        for pid, rec in registry.items():
            expiry = datetime.fromisoformat(rec["expiry_date"]).date()
            days   = (expiry - today).days
            if days < 0:
                expired.append(pid)
            elif days <= self.warn_days:
                expiring.append(f"{pid} ({days}d)")

        if expired:
            return "FAIL", f"{len(expired)} expired IRB protocol(s): {expired}"
        if expiring:
            return "WARN", f"Protocol(s) expiring soon: {expiring}"
        return "OK", f"{len(registry)} active protocol(s), none expiring within {self.warn_days} days."

    def _check_purpose_registry(self) -> tuple:
        """Verify the purpose limitation registry file exists and is populated."""
        purpose_file = pathlib.Path(self.gov.log_dir) / "purpose_registry.json"  # type: ignore[attr-defined]
        if not purpose_file.exists():
            return "WARN", "Purpose registry not found — PurposeLimitationEnforcer not yet used."
        try:
            data = json.loads(purpose_file.read_text(encoding="utf-8"))
            if not data:
                return "WARN", "Purpose registry exists but is empty."
            return "OK", f"{len(data)} purpose mapping(s) registered."
        except Exception as exc:  # pylint: disable=broad-exception-caught
            return "FAIL", f"Could not read purpose registry: {exc}"

    def _check_vendor_risk(self) -> tuple:
        """Check vendor risk registry for overdue reviews."""
        vendor_file = pathlib.Path(self.gov.log_dir) / "vendor_registry.json"  # type: ignore[attr-defined]
        if not vendor_file.exists():
            return "WARN", "Vendor registry not found — VendorRiskTracker not yet used."
        try:
            registry = json.loads(vendor_file.read_text(encoding="utf-8"))
        except Exception as exc:  # pylint: disable=broad-exception-caught
            return "FAIL", f"Could not read vendor registry: {exc}"
        if not registry:
            return "WARN", "Vendor registry is empty."

        today    = _now_utc().date()
        overdue, due_soon = [], []
        for vid, rec in registry.items():
            raw_date = rec.get("next_review_date", "")
            if not raw_date:
                due_soon.append(f"{vid} (no review date set)")
                continue
            try:
                next_review = datetime.fromisoformat(raw_date).date()
            except (ValueError, TypeError):
                due_soon.append(f"{vid} (invalid date: {raw_date!r})")
                continue
            days = (next_review - today).days
            if days < 0:
                overdue.append(vid)
            elif days <= self.warn_days:
                due_soon.append(f"{vid} ({days}d)")

        if overdue:
            return "FAIL", f"{len(overdue)} overdue vendor review(s): {overdue}"
        if due_soon:
            return "WARN", f"Vendor review(s) due soon: {due_soon}"
        return "OK", f"{len(registry)} vendor(s), all reviews current."

    # ── HTML report ───────────────────────────────────────────────────────────

    def _build_html(self) -> str:
        passed = sum(1 for r in self._results if r["status"] == "OK")
        warned = sum(1 for r in self._results if r["status"] == "WARN")
        failed = sum(1 for r in self._results if r["status"] == "FAIL")
        total  = len(self._results)

        overall_badge = "badge-green" if failed == 0 and warned == 0 \
            else ("badge-yellow" if failed == 0 else "badge-red")
        overall_label = "ALL CONTROLS PASSING" if failed == 0 and warned == 0 \
            else (f"{warned} WARNING(S)" if failed == 0 else f"{failed} CONTROL(S) FAILED")

        buf = io.StringIO()
        buf.write(_html_head("Compliance Controls Status"))
        buf.write("<h1>🔒 Compliance Controls Status</h1>\n")
        buf.write(
            f"<div class='summary-grid'>"
            f"<div class='summary-card'><div class='num'>{total}</div>"
            f"<div class='lbl'>Total Controls</div></div>"
            f"<div class='summary-card'><div class='num' style='color:#28a745'>{passed}</div>"
            f"<div class='lbl'>Passing</div></div>"
            f"<div class='summary-card'><div class='num' style='color:#ffc107'>{warned}</div>"
            f"<div class='lbl'>Warnings</div></div>"
            f"<div class='summary-card'><div class='num' style='color:#dc3545'>{failed}</div>"
            f"<div class='lbl'>Failed</div></div>"
            f"</div>\n"
            f"<p><span class='badge {overall_badge}'>{_he(overall_label)}</span></p>\n"
        )

        buf.write(
            "<h2>Control Results</h2>"
            "<table><tr><th>Control</th><th>Status</th><th>Detail</th>"
            "<th>Checked At</th></tr>\n"
        )
        for r in self._results:
            badge = {"OK": "badge-green", "WARN": "badge-yellow",
                     "FAIL": "badge-red"}.get(r["status"], "badge-blue")
            buf.write(
                f"<tr>"
                f"<td><code>{_he(r['control_id'])}</code></td>"
                f"<td><span class='badge {badge}'>{_he(r['status'])}</span></td>"
                f"<td>{_he(r['detail'])}</td>"
                f"<td>{_he(r['checked_at'][:19])}</td>"
                f"</tr>\n"
            )
        buf.write("</table>\n")
        buf.write(_html_foot())
        return buf.getvalue()


