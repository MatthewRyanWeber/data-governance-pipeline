#  2. VendorRiskTracker  —  third-party vendor security registry
# ═════════════════════════════════════════════════════════════════════════════

class VendorRiskTracker:
    """
    Maintain a registry of third-party vendors and services that receive or
    process data from the pipeline, tracking their security posture, SOC 2
    status, last review date, and the types of data they handle.

    This addresses the vendor risk management gap identified in SOC 2 and
    GDPR Art. 28 (processor agreements).  Every destination loader in
    pipeline_v3.py represents a vendor that should appear in this registry.

    Registry format (vendor_registry.json)
    ───────────────────────────────────────
    Each entry is keyed by vendor_id and contains:
        vendor_name         Human-readable name (e.g. "Snowflake Inc.")
        service_type        What the vendor provides (e.g. "cloud data warehouse")
        data_types          List of data categories sent to this vendor
        soc2_status         "certified" | "in_progress" | "not_certified" | "unknown"
        soc2_report_date    Date of most recent SOC 2 report (ISO string)
        last_review_date    Date of most recent internal vendor review
        next_review_date    Date the next review is due
        dpa_signed          Whether a Data Processing Agreement is signed (bool)
        dpa_date            Date DPA was signed
        risk_level          "low" | "medium" | "high" | "critical"
        notes               Free text notes

    Quick-start
    ───────────
        from compliance_extensions import VendorRiskTracker
        from pipeline_v3 import GovernanceLogger

        gov     = GovernanceLogger(run_id="run_001", src="compliance")
        tracker = VendorRiskTracker(gov)

        tracker.register_vendor(
            vendor_id        = "snowflake_prod",
            vendor_name      = "Snowflake Inc.",
            service_type     = "cloud data warehouse",
            data_types       = ["employee_data", "financial_data"],
            soc2_status      = "certified",
            soc2_report_date = "2024-11-01",
            last_review_date = "2025-01-15",
            next_review_date = "2026-01-15",
            dpa_signed       = True,
            dpa_date         = "2024-01-01",
            risk_level       = "medium",
        )

        tracker.check_vendor("snowflake_prod")
        tracker.export_register("vendor_register.html")
    """

    _REGISTRY_FILE = "vendor_registry.json"
    _VALID_SOC2    = {"certified", "in_progress", "not_certified", "unknown"}
    _VALID_RISK    = {"low", "medium", "high", "critical"}

    def __init__(
        self,
        gov,
        warn_days: int  = 30,
        dry_run:   bool = False,
    ) -> None:
        self.gov       = gov
        self.warn_days = warn_days
        self.dry_run   = dry_run
        self._lock     = threading.Lock()
        self._path     = pathlib.Path(gov.log_dir) / self._REGISTRY_FILE  # type: ignore[attr-defined]
        self._registry: Dict[str, Dict] = self._load()

    # ── Public API ────────────────────────────────────────────────────────────

    def register_vendor(
        self,
        vendor_id:        str,
        vendor_name:      str,
        service_type:     str,
        data_types:       Optional[List[str]] = None,
        soc2_status:      str  = "unknown",
        soc2_report_date: Optional[str] = None,
        last_review_date: Optional[str] = None,
        next_review_date: Optional[str] = None,
        dpa_signed:       bool = False,
        dpa_date:         Optional[str] = None,
        risk_level:       str  = "medium",
        notes:            str  = "",
    ) -> None:
        """Add or update a vendor in the registry."""
        if soc2_status not in self._VALID_SOC2:
            raise ValueError(
                f"VendorRiskTracker: soc2_status must be one of "
                f"{self._VALID_SOC2}, got '{soc2_status}'."
            )
        if risk_level not in self._VALID_RISK:
            raise ValueError(
                f"VendorRiskTracker: risk_level must be one of "
                f"{self._VALID_RISK}, got '{risk_level}'."
            )

        # Default next_review_date to 1 year from today if not provided
        if not next_review_date:
            next_review_date = (
                _now_utc().date() + timedelta(days=365)
            ).isoformat()

        record = {
            "vendor_id":        vendor_id,
            "vendor_name":      vendor_name,
            "service_type":     service_type,
            "data_types":       data_types or [],
            "soc2_status":      soc2_status,
            "soc2_report_date": soc2_report_date or "",
            "last_review_date": last_review_date or _now_utc().date().isoformat(),
            "next_review_date": next_review_date,
            "dpa_signed":       dpa_signed,
            "dpa_date":         dpa_date or "",
            "risk_level":       risk_level,
            "notes":            notes,
            "registered_at":    _now_utc().isoformat(),
        }
        with self._lock:
            self._registry[vendor_id] = record
            self._save()

        self.gov._event(  # type: ignore[attr-defined]
            "COMPLIANCE", "VENDOR_REGISTERED",
            {"vendor_id":   vendor_id,
             "vendor_name": vendor_name,
             "risk_level":  risk_level,
             "soc2_status": soc2_status,
             "dpa_signed":  dpa_signed},
        )
        print(f"  ✅  Vendor registered: {vendor_id} ({vendor_name}) "
              f"— risk: {risk_level}, SOC 2: {soc2_status}")

    def check_vendor(self, vendor_id: str) -> Dict:
        """
        Return the vendor record and log a VENDOR_REVIEW_CHECKED event.
        Warns if the vendor review is overdue or DPA is missing.
        Raises ValueError if vendor_id is not in the registry.
        """
        record = self._registry.get(vendor_id)
        if not record:
            raise ValueError(
                f"VendorRiskTracker: vendor '{vendor_id}' not in registry. "
                f"Register with tracker.register_vendor(...)."
            )

        today       = _now_utc().date()
        next_review = datetime.fromisoformat(record["next_review_date"]).date()
        days_until  = (next_review - today).days

        if days_until < 0:
            logger.warning(
                "VendorRiskTracker: review overdue for %s by %d day(s)",
                vendor_id, abs(days_until)
            )
        elif days_until <= self.warn_days:
            logger.warning(
                "VendorRiskTracker: review for %s due in %d day(s)",
                vendor_id, days_until
            )

        if not record.get("dpa_signed"):
            logger.warning(
                "VendorRiskTracker: no DPA signed for %s — "
                "required for GDPR Art. 28 compliance.", vendor_id
            )

        self.gov._event(  # type: ignore[attr-defined]
            "COMPLIANCE", "VENDOR_REVIEW_CHECKED",
            {"vendor_id":      vendor_id,
             "vendor_name":    record["vendor_name"],
             "risk_level":     record["risk_level"],
             "soc2_status":    record["soc2_status"],
             "dpa_signed":     record["dpa_signed"],
             "next_review":    record["next_review_date"],
             "days_until_review": days_until},
        )
        return record

    def get_overdue_reviews(self) -> List[Dict]:
        """Return all vendors whose review date has passed."""
        today = _now_utc().date()
        return [
            {**rec, "days_overdue": (today - datetime.fromisoformat(
                rec["next_review_date"]).date()).days}
            for rec in self._registry.values()
            if datetime.fromisoformat(rec["next_review_date"]).date() < today
        ]

    def get_high_risk_vendors(self) -> List[Dict]:
        """Return all vendors with risk_level 'high' or 'critical'."""
        return [
            rec for rec in self._registry.values()
            if rec["risk_level"] in ("high", "critical")
        ]

    def get_missing_dpa(self) -> List[Dict]:
        """Return all vendors without a signed DPA."""
        return [
            rec for rec in self._registry.values()
            if not rec.get("dpa_signed")
        ]

    def all_vendors(self) -> List[Dict]:
        return list(self._registry.values())

    def export_register(self, path: Union[str, pathlib.Path]) -> pathlib.Path:
        """Write an HTML vendor risk register."""
        path = pathlib.Path(path)
        path.write_text(self._build_html(), encoding="utf-8")
        logger.info("VendorRiskTracker: register exported → %s", path)
        return path

    # ── Internals ─────────────────────────────────────────────────────────────

    def _load(self) -> Dict:
        if self._path.exists():
            try:
                return json.loads(self._path.read_text(encoding="utf-8"))
            except Exception as exc:  # pylint: disable=broad-exception-caught
                logger.warning(
                    "VendorRiskTracker: could not load registry from %s: %s "
                    "— starting with empty registry.", self._path, exc
                )
        return {}

    def _save(self) -> None:
        self._path.write_text(
            json.dumps(self._registry, indent=2), encoding="utf-8"
        )

    def _build_html(self) -> str:
        today    = _now_utc().date()
        buf      = io.StringIO()
        overdue  = self.get_overdue_reviews()
        no_dpa   = self.get_missing_dpa()
        high_risk = self.get_high_risk_vendors()

        buf.write(_html_head("Vendor Risk Register"))
        buf.write("<h1>🏢 Vendor Risk Register</h1>\n")
        buf.write(
            f"<div class='summary-grid'>"
            f"<div class='summary-card'><div class='num'>{len(self._registry)}</div>"
            f"<div class='lbl'>Total Vendors</div></div>"
            f"<div class='summary-card'><div class='num' style='color:#dc3545'>"
            f"{len(overdue)}</div>"
            f"<div class='lbl'>Overdue Reviews</div></div>"
            f"<div class='summary-card'><div class='num' style='color:#ffc107'>"
            f"{len(no_dpa)}</div>"
            f"<div class='lbl'>Missing DPA</div></div>"
            f"<div class='summary-card'><div class='num' style='color:#dc3545'>"
            f"{len(high_risk)}</div>"
            f"<div class='lbl'>High / Critical Risk</div></div>"
            f"</div>\n"
        )

        if self._registry:
            _soc2_badge = {
                "certified":     "badge-green",
                "in_progress":   "badge-yellow",
                "not_certified": "badge-red",
                "unknown":       "badge-blue",
            }
            _risk_badge = {
                "low":      "badge-green",
                "medium":   "badge-yellow",
                "high":     "badge-red",
                "critical": "badge-red",
            }
            buf.write(
                "<h2>All Vendors</h2>"
                "<table><tr><th>Vendor</th><th>Service</th><th>Risk</th>"
                "<th>SOC 2</th><th>DPA</th><th>Next Review</th>"
                "<th>Data Types</th></tr>\n"
            )
            for rec in sorted(self._registry.values(),
                              key=lambda r: r["risk_level"],
                              reverse=True):
                next_rev  = datetime.fromisoformat(rec["next_review_date"]).date()
                days_left = (next_rev - today).days
                rev_label = (
                    f"<span class='badge badge-red'>OVERDUE {abs(days_left)}d</span>"
                    if days_left < 0 else
                    f"<span class='badge badge-yellow'>{days_left}d</span>"
                    if days_left <= self.warn_days else
                    f"{_he(rec['next_review_date'])}"
                )
                dpa_badge = (
                    "<span class='badge badge-green'>Signed</span>"
                    if rec["dpa_signed"] else
                    "<span class='badge badge-red'>Missing</span>"
                )
                data_str = _he(", ".join(rec.get("data_types", [])) or "—")
                buf.write(
                    f"<tr>"
                    f"<td><strong>{_he(rec['vendor_name'])}</strong><br>"
                    f"<small>{_he(rec['vendor_id'])}</small></td>"
                    f"<td>{_he(rec['service_type'])}</td>"
                    "<td><span class='badge " + _risk_badge.get(rec['risk_level'], 'badge-blue') + "'>"
                    f"{_he(rec['risk_level'].upper())}</span></td>"
                    "<td><span class='badge " + _soc2_badge.get(rec['soc2_status'], 'badge-blue') + "'>"
                    f"{_he(rec['soc2_status'])}</span></td>"
                    f"<td>{dpa_badge}</td>"
                    f"<td>{rev_label}</td>"
                    f"<td>{data_str}</td>"
                    f"</tr>\n"
                )
            buf.write("</table>\n")
        else:
            buf.write("<p>No vendors registered.</p>\n")

        buf.write(_html_foot())
        return buf.getvalue()


