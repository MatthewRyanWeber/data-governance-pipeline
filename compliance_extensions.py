    def __init__(
        self,
        gov,
        org_name:          str  = "Your Organization",
        show_irb_titles:   bool = False,
        include_vendors:   bool = True,
        dry_run:           bool = False,
    ) -> None:
        """
        Parameters
        ──────────
        gov                 GovernanceLogger for audit events.
        org_name            Your organization name — shown in the report header.
        show_irb_titles     If True, IRB study titles are included in the report.
                            Default False — counts only, no study details.
        include_vendors     If True, include vendor risk summary section.
        dry_run             Generate report without logging a TRUST_REPORT_GENERATED
                            event to the audit ledger.
        """
        self.gov              = gov
        self.org_name         = org_name
        self.show_irb_titles  = show_irb_titles
        self.include_vendors  = include_vendors
        self.dry_run          = dry_run
        self._log_dir         = pathlib.Path(gov.log_dir)  # type: ignore[attr-defined]

    # ── Public API ────────────────────────────────────────────────────────────

    def generate(self, path: Union[str, pathlib.Path]) -> pathlib.Path:
        """
        Compile and write the trust report to disk.
        Logs TRUST_REPORT_GENERATED to the governance ledger.
        Returns the path of the written file.
        """
        path = pathlib.Path(path)
        data = self._collect_data()
        path.write_text(self._build_html(data), encoding="utf-8")

        if not self.dry_run:
            self.gov._event(  # type: ignore[attr-defined]
                "COMPLIANCE", "TRUST_REPORT_GENERATED",
                {"path":        str(path),
                 "org_name":    self.org_name,
                 "audit_events": data["ledger"]["total_events"],
                 "baa_count":   data["baa"]["count"],
                 "vendor_count": data["vendors"]["count"]},
            )
        logger.info("TrustReportGenerator: trust report written → %s", path)
        print(f"  ✅  Trust report generated → {path}")
        return path

    # ── Data collection ───────────────────────────────────────────────────────

    def _collect_data(self) -> Dict:
        return {
            "generated_at": _iso(_now_utc()),
            "org_name":     self.org_name,
            "ledger":       self._collect_ledger(),
            "baa":          self._collect_baa(),
            "irb":          self._collect_irb(),
            "consent":      self._collect_consent(),
            "vendors":      self._collect_vendors(),
            "purpose":      self._collect_purpose(),
            "controls":     self._collect_controls(),
        }

    def _collect_ledger(self) -> Dict:
        ledger_files = sorted(self._log_dir.glob("audit_ledger*.jsonl"))
        if not ledger_files:
            return {"total_events": 0, "date_range": "No events yet",
                    "categories": {}}
        events = []
        for f in ledger_files:
            for line in f.read_text(encoding="utf-8").splitlines():
                line = line.strip()
                if line:
                    try:
                        events.append(json.loads(line))
                    except json.JSONDecodeError:
                        pass

        categories: Dict[str, int] = {}
        for ev in events:
            cat = ev.get("category", "UNKNOWN")
            categories[cat] = categories.get(cat, 0) + 1

        timestamps = [ev.get("timestamp_utc", "") for ev in events if ev.get("timestamp_utc")]
        date_range = (
            f"{min(timestamps)[:10]} — {max(timestamps)[:10]}"
            if timestamps else "Unknown"
        )
        return {
            "total_events": len(events),
            "date_range":   date_range,
            "categories":   categories,
        }

    def _collect_baa(self) -> Dict:
        baa_file = self._log_dir / "baa_registry.json"
        if not baa_file.exists():
            return {"count": 0, "active": 0, "next_expiry": "N/A"}
        try:
            registry = json.loads(baa_file.read_text(encoding="utf-8"))
        except Exception:  # pylint: disable=broad-exception-caught
            return {"count": 0, "active": 0, "next_expiry": "N/A"}

        today  = _now_utc().date()
        active = [
            rec for rec in registry.values()
            if datetime.fromisoformat(rec["expiry_date"]).date() >= today
        ]
        expiries = sorted(
            datetime.fromisoformat(r["expiry_date"]).date()
            for r in active
        )
        return {
            "count":       len(registry),
            "active":      len(active),
            "next_expiry": str(expiries[0]) if expiries else "N/A",
        }

    def _collect_irb(self) -> Dict:
        irb_file = self._log_dir / "irb_registry.json"
        if not irb_file.exists():
            return {"count": 0, "active": 0, "titles": []}
        try:
            registry = json.loads(irb_file.read_text(encoding="utf-8"))
        except Exception:  # pylint: disable=broad-exception-caught
            return {"count": 0, "active": 0, "titles": []}

        today  = _now_utc().date()
        active = [
            rec for rec in registry.values()
            if datetime.fromisoformat(rec["expiry_date"]).date() >= today
        ]
        titles = [rec["study_title"] for rec in active] if self.show_irb_titles else []
        return {"count": len(registry), "active": len(active), "titles": titles}

    def _collect_consent(self) -> Dict:
        db_file = self._log_dir / "consent.db"
        if not db_file.exists():
            return {"total_records": 0, "purposes": []}
        try:
            import sqlite3
            conn     = sqlite3.connect(str(db_file), timeout=5)
            total    = conn.execute(
                "SELECT COUNT(*) FROM consent_records"
            ).fetchone()[0]
            purposes = [
                row[0] for row in conn.execute(
                    "SELECT DISTINCT purpose FROM consent_records"
                ).fetchall()
            ]
            conn.close()
            return {"total_records": total, "purposes": purposes}
        except Exception:  # pylint: disable=broad-exception-caught
            return {"total_records": 0, "purposes": []}

    def _collect_vendors(self) -> Dict:
        vendor_file = self._log_dir / "vendor_registry.json"
        if not vendor_file.exists():
            return {"count": 0, "soc2_certified": 0, "dpa_signed": 0}
        try:
            registry = json.loads(vendor_file.read_text(encoding="utf-8"))
        except Exception:  # pylint: disable=broad-exception-caught
            return {"count": 0, "soc2_certified": 0, "dpa_signed": 0}

        soc2_cert = sum(
            1 for r in registry.values() if r.get("soc2_status") == "certified"
        )
        dpa_signed = sum(
            1 for r in registry.values() if r.get("dpa_signed")
        )
        return {
            "count":         len(registry),
            "soc2_certified": soc2_cert,
            "dpa_signed":    dpa_signed,
        }

    def _collect_purpose(self) -> Dict:
        purpose_file = self._log_dir / "purpose_registry.json"
        if not purpose_file.exists():
            return {"count": 0, "purposes": []}
        try:
            data = json.loads(purpose_file.read_text(encoding="utf-8"))
            return {"count": len(data), "purposes": list(data.keys())}
        except Exception:  # pylint: disable=broad-exception-caught
            return {"count": 0, "purposes": []}

    def _collect_controls(self) -> Dict:
        """Read the most recent compliance monitor results if available."""
        results_files = sorted(
            self._log_dir.glob("compliance_status*.json"),
            reverse=True,
        )
        if not results_files:
            return {"available": False, "results": []}
        try:
            results = json.loads(results_files[0].read_text(encoding="utf-8"))
            return {"available": True, "results": results,
                    "file": results_files[0].name}
        except Exception:  # pylint: disable=broad-exception-caught
            return {"available": False, "results": []}

